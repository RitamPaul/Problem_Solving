class Solution {
  public:
    int countSubarrays(vector<int> &arr, int k) {
        // code here
        unordered_map<int,int> mp;
        int prefix=0, ans=0;
        for(auto &i : arr){
            prefix += i;
            if(prefix == k)
                ++ans;
            if(mp.count(prefix-k))
                ans += mp[prefix-k];
            ++mp[prefix];
        }
        return ans;
    }
};

