class Solution{
  public:
  int smallestSumSubarray(vector<int>& a){
      //Code here
      int n=a.size();
      int continuous_sum=0, mini = INT_MAX;
      for(int i=0; i<n; ++i){
          continuous_sum = min(continuous_sum+a[i], a[i]);
          mini = min(mini, continuous_sum);
      }
      return mini;
  }
};


