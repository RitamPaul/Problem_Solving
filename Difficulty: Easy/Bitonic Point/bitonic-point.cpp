// User function template for C++
class Solution {
  public:
    int findMaximum(vector<int> &arr) {
        // code here
        int n=arr.size();
        int left, right;
        for(int i=0; i<n; ++i){
            left=right=INT_MIN;
            if(i-1>=0)
                left=arr[i-1];
            if(i+1<n)
                right=arr[i+1];
            if(left<arr[i] and arr[i]>right)
                return arr[i];
        }
        return -1;
    }
};


