class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        // Code Here
        int n1=INT_MIN, n2=INT_MIN;
        for(auto &i : arr){
            if(i > n1){
                n2 = max(n2, n1);
                n1 = i;
            }
            else if(i != n1)
                n2 = max(n2, i);
        }
        return n2==INT_MIN ? -1 : n2;
    }
};

